package operators;

public class AssignmentOperator {

	public static void main(String[] args) {

		int a =10;
		System.out.println(a);
		int x,y,z;
		x=y=z=30;
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		
		//compound operator
		 int d=50;
		 d += 10; //d =d+10
		 System.out.println(d);
		
	}

}
